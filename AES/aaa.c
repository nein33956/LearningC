ADC_HandleTypeDef hadc1;
UART_HandleTypeDef huart1;

uint16_t duLieu[3];
uint8_t truyenDuLieu[200];
float dienAp[2];
float nhietDo = 0;
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_ADC1_Init(void);
static void MX_USART1_UART_Init(void);

int main(void){
	char data[100];
	HAL_Init();
	SystemClock_Config();
	MX_GPIO_Init();
	MX_ADC1_Init();
	MX_USART1_UART_Init();

	while (1){
		HAL_ADC_Start(&hadc1);
		for(uint8_t i = 0 ; i <26; i++ ) asm("NOP");

		duLieu[0]= HAL_ADC_GetValue(&hadc1);

		for(uint8_t i = 0 ; i < 60; i++ ) asm("NOP");

		duLieu[1]= HAL_ADC_GetValue(&hadc1);

		for(uint8_t i = 0 ; i < 200; i++ ) asm("NOP");

		duLieu[2]= HAL_ADC_GetValue(&hadc1);

		dienAp[0] = duLieu[0] * 3.3 / 4095;
		dienAp[1] = duLieu[1] * 3.3 / 4095;
		nhietDo = ((1.43 - (3.3/4095)*duLieu[2])/0.0043)+10;

		sprintf((char *)truyenDuLieu, "Dien ap 1 =%.2fV| Dien ap 2 =%.2fV| Nhiet do =%.2fC\n", dienAp[0], dienAp[1], nhietDo);

		HAL_UART_Transmit(&huart1, (uint8_t *)truyenDuLieu, strlen((char *)truyenDuLieu), 100);

		HAL_Delay(1000);
	}
}

